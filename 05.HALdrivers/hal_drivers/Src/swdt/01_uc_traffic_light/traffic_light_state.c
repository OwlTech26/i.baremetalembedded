void TRAFFIC_LIGHT_STATE_init(void)
{
	G_TL_STATE = e_tl_st_idle;
	G_TL_STATE_UPDATED = FALSE;
}

const t_tl_state TRAFFIC_LIGHT_STATE_getter(void)
{
	return G_TL_STATE;
}

void TRAFFIC_LIGHT_STATE_setter(const t_tl_state curr_state)
{
	G_TL_STATE_UPDATED = TRUE;

	G_TL_STATE = curr_state;
}

const t_bool TRAFFIC_LIGHT_STATE_changed(void)
{
	const t_bool ret_state_update = G_TL_STATE_UPDATED;
	if (ret_state_update != FALSE) {
		G_TL_STATE_UPDATED = FALSE;
	}
	return ret_state_update;
}

